const KNOWN_SONG_ORDER = ["birthday", "jasmine", "chilege", "bluebird", "katyusha"];
const knownSongRank = new Map(KNOWN_SONG_ORDER.map((id, index) => [id, index]));

const songsDirUrl = new URL("../data/songs/", import.meta.url);
const outputUrl = new URL("../src/songs.js", import.meta.url);

function compareSongIds(a, b) {
  const aRank = knownSongRank.has(a) ? knownSongRank.get(a) : Number.POSITIVE_INFINITY;
  const bRank = knownSongRank.has(b) ? knownSongRank.get(b) : Number.POSITIVE_INFINITY;

  if (aRank !== bRank) {
    return aRank - bRank;
  }

  return a.localeCompare(b, "en");
}

export async function readSongFiles() {
  const entries = await readdir(songsDirUrl, { withFileTypes: true });
  const jsonFiles = entries
    .filter((entry) => entry.isFile() && entry.name.endsWith(".json"))
    .map((entry) => entry.name)
    .sort((a, b) => compareSongIds(a.slice(0, -5), b.slice(0, -5)));

  return Promise.all(
    jsonFiles.map(async (fileName) => {
      const idFromFile = fileName.slice(0, -5);
      const fileUrl = new URL(fileName, songsDirUrl);
      const raw = await readFile(fileUrl, "utf8");
      return {
        fileName,
        idFromFile,
        raw,
        song: JSON.parse(raw)
      };
    })
  );
}

function indent(text, spaces) {
  const prefix = " ".repeat(spaces);
  return text
    .split("\n")
    .map((line) => `${prefix}${line}`)
    .join("\n");
}

function buildSongsModule(songs) {
  const displayKeys = indent(JSON.stringify(DISPLAY_KEYS, null, 2), 2);
  const songEntries = songs
    .map(({ song }) => {
      const songJson = indent(JSON.stringify(song, null, 2), 4);
      return `  ${JSON.stringify(song.id)}: ${songJson.trimStart()}`;
    })
    .join(",\n");

  return `// This file is generated by scripts/build-song-library.mjs. Do not edit song data here.\n\nexport const DISPLAY_KEYS = ${displayKeys.trimStart()};\n\nexport const NOTE_INDEX = new Map(DISPLAY_KEYS.map((note, index) => [note.name, index]));\n\nexport const SONG_LIBRARY = {\n${songEntries}\n};\n`;
}

export async function buildSongLibrary() {
  const songs = await readSongFiles();
  const output = buildSongsModule(songs);
  await writeFile(outputUrl, output, "utf8");
  return songs.length;
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  const count = await buildSongLibrary();
  console.log(`Built src/songs.js from ${count} song JSON files.`);
}
